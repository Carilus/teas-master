                    <h4>Read the stimulus strategically.</h4>
                    <p class="pb-20">Strategic reading requires paying particular attention to the stimulis’ topic and scope. The topic refers to what the author is writing about, while the scope refers to the author’s interest in a specific aspect of the topic. Candidates can find the topic and scope in the first few sentences of the passage or labels, headings, and titles (for figures and graphs).</p>
                    <p>Candidates should also try and understand the author’s purpose for writing a passage. Their purpose is often to outline information, describe a topic, or explain a process. However, the author can also use their work to try and persuade the reader to observe situations from a specific point of view.</p>
                 </div>
              </div>
              <p>Candidates should note that some questions won’t refer to a stimulus, and individuals who find such questions should proceed to the second step. Additionally, candidates who find one question on a stimulus should read the question before reading it to research their answer.</p>
              <h4>Analyze the question</h4>
              <p>You should determine what a question demands from you before searching for the correct answer. For instance, the question could ask for the author’s primary purpose or main point in writing. The question could also be asking for details from the stimulus or why an author decided to include some feature or detail. Thus, the task in the question determines how you will research the stimulus, map, and what to look for.</p>
              <h4>Research</h4>
              <p>You should research your answers in the stimulus. Candidates who map their stimulus can find their answers more quickly. The map helps you find the right place to find your answer on the stimulus more quickly, allowing you to read the appropriate portion of your stimulus as needed.</p>
              <p>Candidates reading their stimulus for the first time after reading the question should read it strategically to find the correct answer. Most TEAS reading questions require candidates to interpret a detail in context, grasp the stimulus’ bigger picture, or find the connection between different details.</p>
              <h4>Predict the answer</h4>
              <p>Candidates should try and predict the correct answer before looking at the choices. Having a clear answer before looking at the choices helps you choose the correct answer without being misled by answers that sound right but aren’t. Additionally, some questions are open-ended, and candidates should research their stimulus using their notes to find a correct answer that aligns with their mental checklist.</p>
              <h4>Evaluate the answers</h4>
              <p>Finally, candidates should evaluate the answers to find one matching their predictions. Try steps 2-4 and refine your thinking if the expected answers are unavailable.</p>
