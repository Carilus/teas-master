                    <h4>Identifying the Main Idea</h4>
                    <p class="pb-15">Several TEAS questions ask candidates to identify the primary idea that the author is writing about or making in the stimulus. Candidates can identify the idea by asking themselves what the author wants them to know and why they want them to know it. Sometimes, the author could be explaining or describing a topic or trying to persuade the reader to ascribe to a particular perspective.</p>
